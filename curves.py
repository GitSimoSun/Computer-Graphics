	def hermit_graph_plot(self, hermit_graph):
		p1, p2 = self.hermit_p1.text(), self.hermit_p2.text()
		r1, r2 = self.hermit_r1.text(), self.hermit_r2.text()
		if (len(p1) == 0) or (len(p2) == 0) or (len(r1) == 0) or (len(r2) == 0):
			self.missing_data_dialog('Tous les données')
			return
		points = [p1, p2, r1, r2]
		test = 1
		for i in range(4):
			points[i] = self.normalize_formula(points[i], f'p{i+1}')
