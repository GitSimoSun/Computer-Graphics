class PlotCanvas(FigureCanvas):
	def __init__(self, parent = None, width = 5, height = 4, dpi = 100):
		self.fig = Figure(figsize = (width, height), dpi= dpi)
		self.ax = self.fig.add_subplot(111)
		FigureCanvas.__init__(self, self.fig)
		self.setParent(parent)
		FigureCanvas.setSizePolicy(self, QSizePolicy.Expanding, QSizePolicy.Expanding)
		FigureCanvas.updateGeometry(self)

	def plot_hermit(self, data):
		self.ax.remove()
		self.ax = self.fig.add_subplot(111)
		self.ax.cla()
		self.ax.set_title('Hermit Curve')
		t = symbols('t')
		qf = Q(*data)
		xm = max(abs(data[0]), abs(data[1])) + 3
		x = np.linspace(-xm, xm, 1000)
		y = lambdify(t, qf)(x)
		self.ax.plot(x, np.transpose(y), 'r')
		self.ax.scatter(data[0], data[2])
		self.ax.scatter(data[1], data[3])
		self.ax.annotate(f'{data[0], data[2]}', (data[0], data[2]))
		self.ax.annotate(f'{data[1], data[3]}', (data[1], data[3]))
		ym = max(abs(data[2]), abs(data[3])) + 5
		self.ax.set_ylim([-ym , ym])
		self.ax.spines['left'].set_position('zero')
		self.ax.spines['right'].set_color('none')
		self.ax.yaxis.tick_left()
		self.ax.spines['bottom'].set_position('zero')
		self.ax.spines['top'].set_color('none')
		self.ax.xaxis.tick_bottom()
		self.draw()

	def plot_bezier(self, p):
		t = symbols('t')
		qf = Q_bezier(p)
		n = len(p)
		d = len(p[0])
		if d == 2:
			self.ax.remove()
			self.ax = self.fig.add_subplot(111)
			self.ax.cla()
			self.ax.set_title('Bézier Curve')
			for i in range(n -1):
				k = (i+1)
				plot_line(self.ax, p[i], p[k], 'r')
			ymin = min(p, key = lambda tup : tup[1])[1]
			ymax = max(p, key = lambda tup : tup[1])[1]
			xmin = min(p, key = lambda tup : tup[0])[0]
			xmax = max(p, key = lambda tup : tup[0])[0]
			tt = np.linspace(0, 1, 1000)
			x = lambdify(t, qf[0])(tt)
			y = lambdify(t, qf[1])(tt)
			self.ax.plot(x, y)
			self.ax.set_ylim([ymin - 1, ymax + 1])
			self.ax.set_xlim([xmin - 1, xmax + 1])
			self.ax.spines['left'].set_position('zero')
			self.ax.spines['right'].set_color('none')
			self.ax.yaxis.tick_left()
			self.ax.spines['bottom'].set_position('zero')
			self.ax.spines['top'].set_color('none')
			self.ax.xaxis.tick_bottom()
		else:
			self.ax.remove()
			self.ax = self.fig.add_subplot(111, projection='3d')
			self.ax.cla()
			self.ax.set_title('Bézier Curve')
			tt = np.linspace(0, 1, 1000)
			X = lambdify(t, qf[0])(tt)
			Y = lambdify(t, qf[1])(tt)
			Z = lambdify(t, qf[2])(tt)
			if not type(Z).__module__ == np.__name__:
				Z = np.array([0] * 1000)
			x = np.reshape(X, (25, 40))
			y = np.reshape(Y, (25, 40))
			z = np.reshape(Z, (25, 40))
			self.ax.plot_wireframe(x,y,z)
		self.draw()

	def plot_ibspline(self, p, boundry_condition, c1, c2):
		self.ax.remove()
		self.ax = self.fig.add_subplot(111)
		self.ax.cla()
		self.ax.set_title('B-spline Interpolation Curve')
		n = len(p)
		for i in range(n -1):
			k = (i+1)
			plot_line(self.ax, p[i], p[k], 'r')
		ymin = min(p, key = lambda tup : tup[1])[1]
		ymax = max(p, key = lambda tup : tup[1])[1]
		xmin = min(p, key = lambda tup : tup[0])[0]
		xmax = max(p, key = lambda tup : tup[0])[0]
		x = np.linspace(xmin, xmax, 1000)
		y = [s_ibspline(t, p, boundry_condition, c1, c2) for t in x]
		self.ax.plot(x, y)
		self.ax.spines['left'].set_position('zero')
		self.ax.spines['right'].set_color('none')
		self.ax.yaxis.tick_left()
		self.ax.spines['bottom'].set_position('zero')
		self.ax.spines['top'].set_color('none')
		self.ax.xaxis.tick_bottom()
		self.draw()

	def plot_abspline(self, p):
		n = len(p)
		d = len(p[0])
		if d == 2:
			self.ax.remove()
			self.ax = self.fig.add_subplot(111)
			self.ax.set_title('B-Spline Approximation Curve')
			for i in range(n -1):
				k = (i+1)
				plot_line(self.ax, p[i], p[k], 'r')
			t = np.linspace(0, 1, 1000, endpoint = False)
			x = [S_abspline(p, 2, k)[0] for k in t]
			y = [S_abspline(p, 2, k)[1] for k in t]
			self.ax.plot(x, y, 'b')
			self.ax.spines['left'].set_position('zero')
			self.ax.spines['right'].set_color('none')
			self.ax.yaxis.tick_left()
			self.ax.spines['bottom'].set_position('zero')
			self.ax.spines['top'].set_color('none')
			self.ax.xaxis.tick_bottom()
		else:
			self.ax.remove()
			self.ax = self.fig.add_subplot(111, projection='3d')
			self.ax.set_title('B-Spline Approximation Curve')
			t = np.linspace(0, 1, 1000, endpoint = False)
			result = np.array([S_abspline(p, 2, k) for k in t])
			X = [r[0] for r in result]
			Y = [r[1] for r in result]
			Z = [r[2] for r in result]
			if not type(Z).__module__ == np.__name__:
				Z = np.array([0] * 1000)
			x = np.reshape(X, (25, 40))
			y = np.reshape(Y, (25, 40))
			z = np.reshape(Z, (25, 40))
			self.ax.plot_wireframe(x,y,z)
		self.draw()
	
class inputFocusFilter(QObject):
	focusIn = pyqtSignal(object)
	def eventFilter(self, widget, event):
		if event.type() == QEvent.FocusIn and isinstance(widget, QLineEdit):
			self.focusIn.emit(widget)
		return super(inputFocusFilter, self).eventFilter(widget, event)


class MyApplication(QtWidgets.QApplication):
	def __init__(self, *arg, **kwarg):
		super(MyApplication, self).__init__(*arg, **kwarg)

		self._input_focus_widget = None

		self.event_filter = inputFocusFilter()
		self.event_filter.focusIn.connect(self.setInputFocusWidget)
		self.installEventFilter(self.event_filter)

	def setInputFocusWidget(self, widget):
		self._input_focus_widget = widget

	def inputFocusWidget(self):
		return self._input_focus_widget



